
 -- 5 PREMIOS NOBEL MAS RECIENTES
SELECT *
 FROM DANIELYN.NOBEL 
 INNER JOIN DANIELYN.YEAR yr ON DANIELYN.NOBEL.NOBEL_ID = yr.YEAR_ID WHERE yr.YEAR_ID > 5;
 
 -- ultimos 10 premios nobel de fisica
 
