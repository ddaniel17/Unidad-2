CREATE OR REPLACE FUNCTION EXI_023664.YEARS (EDAD1 IN DATE)
    RETURN NUMBER
AS
    AÑOS   NUMBER;
BEGIN
    SELECT FLOOR (MONTHS_BETWEEN (SYSDATE, EDAD1) / 12) INTO AÑOS FROM DUAL;

    RETURN AÑOS;
END YEARS;

SELECT WIN.WINNER_NAME, EXI_023664.YEAR (WIN.BIRTHDATE) Años
